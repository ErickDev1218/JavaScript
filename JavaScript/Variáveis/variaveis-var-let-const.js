//var

var clima = "Quente"
clima = "Frio" // JS ler as linhas de códigos de cima pra baixo, deixando possivel "atualizar" a 'var clima'
console.log(clima)

let horas = "13h25min"
horas = "14h"
console.log(horas)
//'let' funciona bem semelhante ao 'var'

const salario = 2000
console.log(salario)
//Diferente de 'var' e 'let', a 'const' é LITERALMENTE constante, não me permitindo fazer atualizações na sua 'timeline'